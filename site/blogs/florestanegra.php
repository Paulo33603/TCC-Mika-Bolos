                    <h2 class="text-center">Modo de preparo</h2><br>
                </div>
            </div>
            <div class="row">
                <div class="col-md-4">
                    <b>Massa:</b><br>
                    <b>1.</b>  Pré-aqueça o forno em temperatura média (180⁰C).<br>
                    <b>2.</b>  Unte e enfarinhe uma fôrma de furo central média (20 cm de diâmetro).<br>
                    <b>3.</b>  Na tigela da batedeira, coloque a margarina, o açúcar e os ovos,
                    e bata até formar um creme fofo e homogêneo. Junte a farinha de trigo,
                    o amido de milho e o chocolate em pó, altemando com o leite, e bata até obter uma massa cremosa.<br>
                    <b>4.</b>  Acrescente o fermento e misture delicadamente.<br>
                    <b>5.</b>  Disponha a massa na fôrma reservada e leve ao forno por 35 minutos,
                    ou até que um palito, depois de espetado na massa saia limpo.<br>
                    <b>6.</b>  Retire do forno, deixe esfriar, desenforme sobre um prato grande.
                    Corte o bolo ao meio, no sentido horizontal, e reserve.<br>
                </div>
                <div class="col-md-4">
                    <b>Recheio</b><br>
                    <b>8.</b>  Na tigela da batedeira, coloque o creme de leite e o açúcar, e bata até obter o ponto de chantilly.<br>
                    <b>9.</b>  Com o auxilio de uma espátula espalhe metade do chantilly em uma das partes do bolo,
                    cubra com metade das cerejas e disponha, por cima, outra parte do bolo.<br>
                    <b>11.</b>  Finalize com o restante do chantilly e decore com as raspas de chocolate e as cerejas restantes.<br>
                    <b>11.</b>  Leve à geladeira por 1 hora, ou até ficar firme. Sirva a seguir.<br>
                    <b>11.</b>  Rendimento: 12 porções.<br>
                </div>
                <div class="col-md-4">
                    <b>Montagem</b><br>
                    <b>10.</b>  Parta o bolo ao meio, com o auxílio de uma faca ou linha de costura.<br>
                    <b>11.</b>  Regue as duas metades do bolo com suco de sua preferência ou guaraná, até que fique bem molhadinho.<br>
                    <b>11.</b>  Espalhe o recheio, coloque a outra metade do bolo por cima, pressionando e passando uma faca na lateral para remover o excesso de recheio.<br>
                    <b>11.</b>  Cubra com chantilly e decore com os morangos partidos ao meio.<br>
                    <b>11.</b>  Leve à geladeira por 2 horas.<br>
